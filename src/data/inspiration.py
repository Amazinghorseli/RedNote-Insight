def get_inspiration(category: str = None) -> list[dict]:
    """
    获取灵感列表。不传 category 返回全部，传了只返回该品类。

    Returns:
        [{"keyword": "辣条", "category": "食品", "type": "both", "tip": "..."}, ...]
    """
    result = []
    categories = [category] if category and category in INSPIRATION else list(INSPIRATION.keys())

    for cat in categories:
        items = INSPIRATION.get(cat, [])
        for idx, item in enumerate(items):
            result.append({
                "keyword": item["keyword"],
                "type": item["type"],
                "tip": item["tip"],
                "category": cat,
                "rank": idx + 1,
                "hots": max(95 - idx * 4, 30),
                "trend": "up" if idx < 3 else "stable",
            })

    result.sort(key=lambda x: -x["hots"])
    for i, r in enumerate(result):
        r["rank"] = i + 1

    return result


def get_categories() -> list[str]:
    """返回所有支持的品类"""
    return list(INSPIRATION.keys())
